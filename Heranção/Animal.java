package Heranção;

public class Animal {
private String especi;
private boolean estimacao;
private int peso;

public void barulho() {
	
}

public String getEspeci() {
	return especi;
}

public void setEspeci(String especi) {
	this.especi = especi;
}

public boolean isEstimacao() {
	return estimacao;
}

public void setEstimacao(boolean estimacao) {
	this.estimacao = estimacao;
}

public int getPeso() {
	return peso;
}

public void setPeso(int peso) {
	this.peso = peso;
}

}
